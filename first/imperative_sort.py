def sort_list_imperative(numbers):
    n = len(numbers)
    for i in range(n - 1):
        for j in range(n - i - 1):
            if numbers[j] < numbers[j + 1]:
                # –û–±–º–µ–Ω –∑–Ω–∞—á–µ–Ω–∏—è–º–∏
                numbers[j], numbers[j + 1] = numbers[j + 1], numbers[j]


numbers = [5, 2, 8, 1, 9]
sort_list_imperative(numbers)
print(numbers)  # –í—ã–≤–æ–¥–∏—Ç [9, 8, 5, 2, 1]
